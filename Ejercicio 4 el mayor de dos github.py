# Primero: pedimos el primer número
num1 = int(input("Escribe el primer número: "))

# Segundo: pedimos el segundo número
num2 = int(input("Escribe el segundo número: "))

# Tercero: comparamos los dos números
if num1 > num2:
    # Cuarto: si el primero es mayor
    print("El número mayor es:", num1)
else:
    # Quinto: si el segundo es mayor
    print("El número mayor es:", num2)